// 논리 연산자
fun main(args:Array<String>) {
    // && 연산자: AND
    println(true && true) // true
    println(true && false) // false
    println(false && true) // false
    println(false && false) // false

    // || 연산자: OR
    println(true || true) // true
    println(true || false) // true
    println(false || true) // true
    println(false || false) // false

    // ! 연산자: NOT
    println(!true) // false
    println(!false) // true
}
